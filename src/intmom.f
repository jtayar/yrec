      SUBROUTINE INTMOM(A,ETA2X,QR0R,R0R,HIM,QIWM)
      use settings, only : CC23  ! COMMON/CONST1/

      IMPLICIT REAL*8(A-H,O-Z)
      DIMENSION C(5)
      DATA C/0.6D0,0.72857142857143D0,0.50884115884115D0,
     *             0.49728653699242D0,0.42852079651326D0/
      SAVE

C  RETURN MOMENT OF INERTIA PER UNIT MASS AT THE SPECIFIED POINT(HI)
C  AND DI/D OMEGA PER UNIT MASS(QIW)
      H1 = CC23*QR0R*R0R
      SUM = 0.0D0
      SUM2 = 0.0D0
      DO 10 J = 1,5
         SUM = SUM + C(J)*(J*ETA2X + 5.0D0)*A**J
         SUM2 = SUM2 + J*C(J)*(J*ETA2X + 5.0D0)*A**J
   10 CONTINUE
      HIM = H1*(1.0D0 + 1.5D-1*SUM)
      QIWM = H1*(1.5D-1*SUM2)

      RETURN
      END
